public class zapato {
        private int id;
        private String nombre;
        private String descripcion;
        private double precio;

    public int getId() {
        return id;
    }
    public String getNombre() {
        return nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public double getPrecio() {
        return precio;
    }
}
